using System;

namespace LatihanPengulanganWhile
{
    class Program
    {
        static void Main(string[] args)
        {
            int i, max_count;
            i = 1;
            max_count = 100;
            while(i < max_count)
            {
                Console.WriteLine("Hello World!");
                i++;
            }
        }
    }
}
